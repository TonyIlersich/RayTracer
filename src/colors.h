#ifndef COLORS_H
#define COLORS_H

#include "includes.h"

// grayscale
constexpr glm::vec3 black   = glm::vec3(0.0f);
constexpr glm::vec3 gray    = glm::vec3(0.5f);
constexpr glm::vec3 white   = glm::vec3(1.0f);

// primary
constexpr glm::vec3 red     = glm::vec3(1, 0, 0);
constexpr glm::vec3 green   = glm::vec3(0, 1, 0);
constexpr glm::vec3 blue    = glm::vec3(0, 0, 1);

// secondary
constexpr glm::vec3 yellow  = glm::vec3(1, 1, 0);
constexpr glm::vec3 cyan    = glm::vec3(0, 1, 1);
constexpr glm::vec3 magenta = glm::vec3(1, 0, 1);

// tertiary
constexpr glm::vec3 orange  = glm::vec3(1.0f, 0.5f, 0.0f);
constexpr glm::vec3 purple  = glm::vec3(0.5f, 0.0f, 1.0f);

#endif